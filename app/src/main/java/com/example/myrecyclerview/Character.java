package com.example.myrecyclerview;

class Character {
    private String name;
    private String desc;
    private String photo;

    String getName() {
        return name;
    }

    void setName(String name) {
        this.name = name;
    }

    String getDesc() {
        return desc;
    }

    void setDesc(String desc) {
        this.desc = desc;
    }

    String getPhoto() {
        return photo;
    }

    void setPhoto(String photo) {
        this.photo = photo;
    }

}
